const CYCLE_MS = MINUTE_DURATIONS_MS.reduce((a, b) => a + b, 0); // 4322349

// START_CUM_MS[k] = real ms elapsed at the moment in-game time becomes minute-of-day k.
// Length 1441 (entries 0..1440 inclusive). START_CUM_MS[h*60] = ms at HH:00.
const START_CUM_MS = [0];
for (const d of MINUTE_DURATIONS_MS) START_CUM_MS.push(START_CUM_MS[START_CUM_MS.length - 1] + d);

// Auto-anchor: real-world Unix ms at the moment in-game was 00:00:00.
// Computed from row[1] of minutes_final.csv: at recording_start + 2633ms,
// in-game was 05:52, which back-fits to anchor 1777571991115 ms.
const ANCHOR_MS = 1777571991115;

function realToIngame(elapsedRealMs) {
  const e = ((elapsedRealMs % CYCLE_MS) + CYCLE_MS) % CYCLE_MS;
  // Walk minutes (1440 iterations max, microseconds in practice).
  let k = 0;
  while (k < 1439 && START_CUM_MS[k + 1] <= e) k++;
  const intoMinute = e - START_CUM_MS[k];
  const minuteFraction = intoMinute / MINUTE_DURATIONS_MS[k];
  const second = Math.floor(minuteFraction * 60);
  const hour = Math.floor(k / 60);
  const minute = k % 60;
  return {
    hour: hour,
    minute: minute,
    second: second,
    fractionOfDay: e / CYCLE_MS,
    fractionOfHour: ((minute * 60 + second) / 3600),
  };
}

function pad(n, w) {
  if (w === undefined) w = 2;
  return String(n).padStart(w, "0");
}

function formatIngame(t) {
  return pad(t.hour) + ":" + pad(t.minute) + ":" + pad(t.second);
}

function phaseLabel(hour) {
  if (hour >= 5 && hour < 7) return "Dawn";
  if (hour >= 7 && hour < 18) return "Day";
  if (hour >= 18 && hour < 20) return "Dusk";
  return "Night";
}

function format12h(date) {
  let h = date.getHours();
  const m = date.getMinutes();
  const ampm = h >= 12 ? "pm" : "am";
  h = h % 12;
  if (h === 0) h = 12;
  return h + ":" + pad(m) + ampm;
}

function formatOffset(seconds) {
  const s = Math.max(0, Math.round(seconds));
  if (s < 60) return s + "s";
  const m = Math.floor(s / 60);
  if (m < 60) return m + "min";
  const h = Math.floor(m / 60);
  const rem = m % 60;
  if (rem === 0) return h + "hr";
  return h + "hr" + rem + "min";
}

// Real-time when in-game hour h:00 starts, for the entry at index i in a timeline
// that begins at currentHour (so i=0 -> current hour, in the current cycle).
function timelineHourStart(i, currentHour, nowMs) {
  const elapsedMs = nowMs - ANCHOR_MS;
  const cyclesPassed = Math.floor(elapsedMs / CYCLE_MS);
  const cycleStartMs = ANCHOR_MS + cyclesPassed * CYCLE_MS;
  const cycleOffset = Math.floor((currentHour + i) / 24);
  const h = (currentHour + i) % 24;
  // START_CUM_MS is now indexed by minute (0..1440); HH:00 is at index h*60.
  return new Date(cycleStartMs + cycleOffset * CYCLE_MS + START_CUM_MS[h * 60]);
}

